function cGasMix = cGasMixFcn(Cgas)
% -------------------------------------------------------------------------
    % cGasMix function 
    % ----------------------------| input |--------------------------------
    %    Cgas = vector with concentration for each species        [mol/cm3]
    % ----------------------------| output |-------------------------------
    % cGasMix = gas mixing concentration                          [mol/cm3]
% -------------------------------------------------------------------------

         cGasMix =  sum(Cgas,2);

% -------------------------------------------------------------------------
end
